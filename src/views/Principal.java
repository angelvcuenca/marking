    public void roles_user(String id_empleado) {

        Connection acceso = con.conexion();
        try {
            PreparedStatement ps = acceso.prepareStatement("SELECT id_rol, tipo_usuario,id_empleado\n"
                    + "FROM cat_roles\n"
                    + "WHERE id_empleado = ? ");
            ps.setString(1, id_empleado);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                if (rs.getString(2).equals("Vista CxC")) {
                    menu_cxc.setVisible(true);
                }
                if (rs.getString(2).equals("Vista Plan Acumulativo")) {
                    menu_plan_acumulativo.setVisible(true);
                }
                if (rs.getString(2).equals("Vista Productos")) {
                    menu_producto.setVisible(true);
                }
                if (rs.getString(2).equals("Vista Cliente")) {
                    menu_cliente.setVisible(true);
                }
                if (rs.getString(2).equals("Vista Usuarios")) {
                    menu_usuarios.setVisible(true);
                }
                if (rs.getString(2).equals("Vista Informes")) {
                    menu_informes.setVisible(true);
                }
                if (rs.getString(2).equals("Vista Configuraciones")) {
                    menu_config.setVisible(true);
                }

            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        } finally {
            con.desconectar();
        }
    }

